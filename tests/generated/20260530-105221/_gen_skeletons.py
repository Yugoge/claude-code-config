#!/usr/bin/env python3
"""Test-writer skeleton generator for task 20260530-105221.
Reads acceptance-criteria JSON, emits one pytest skeleton per active AC,
writes the per-task manifest, and upserts the global index. Self-deletes
nothing; the orchestrator removes this helper after run.
"""
import json
import re
import datetime
from pathlib import Path

ROOT = Path("/dev/shm/dev-workspace/dot-claude")
TASK_ID = "20260530-105221"
AC_PATH = ROOT / "docs/dev/acceptance-criteria-20260530-105221.json"
OUT_DIR = ROOT / "tests/generated" / TASK_ID
GLOBAL_INDEX = ROOT / "tests/generated/manifest.json"
PER_TASK_MANIFEST = OUT_DIR / "manifest.json"

NOW = datetime.datetime.now(datetime.timezone.utc).isoformat()

CANONICAL_REQUIRED = {
    "ui": ["url", "viewports", "selectors", "assertions"],
    "api": ["endpoint", "method", "request", "assertions"],
    "data": ["file", "assertions"],
    "hook": ["script", "args", "assertions"],
}


def normalize_id(ac_id: str) -> str:
    return re.sub(r"[^0-9A-Za-z_]", "_", ac_id)


def one_line(s: str, n: int = 160) -> str:
    s = " ".join(str(s).split())
    return s if len(s) <= n else s[: n - 1] + "…"


def skeleton(ac: dict) -> str:
    ac_id = ac["id"]
    ac_uid = ac["ac_uid"]
    ac_type = ac["type"]
    idn = normalize_id(ac_id)
    given = ac.get("given", "")
    when = ac.get("when", "")
    then = ac.get("then", "")
    summary = one_line(f"{ac_id} {when}")
    # Escape any triple-quote / backslash hazards for the docstring.
    g = one_line(given, 400)
    w = one_line(when, 400)
    t = one_line(then, 700)
    return f'''# Auto-generated by agents/test-writer.md from docs/dev/acceptance-criteria-20260530-105221.json
# AC ID: {ac_id}  ac_uid: {ac_uid}  type: {ac_type}
# Dev is free to REPLACE the body of test_{idn}() with a real
# implementation that asserts the GIVEN/WHEN/THEN behaviour. The metadata
# above (AC_UID, AC_TYPE, docstring) MUST be preserved verbatim so QA can
# trace each test back to its source AC entry.

import pytest

AC_UID = "{ac_uid}"
AC_TYPE = "{ac_type}"


def test_{idn}():
    """
    GIVEN: {g}
    WHEN:  {w}
    THEN:  {t}
    """
    # TODO(dev): replace the line below with the real test body. While the
    # TEST_INCOMPLETE sentinel is present the test will hard-fail, marking
    # the AC as unimplemented for QA Phase 5.
    pytest.fail(f"TEST_INCOMPLETE: {{AC_UID}} — {summary}")
'''


def main():
    data = json.loads(AC_PATH.read_text())
    acs = data["acceptance_criteria"]

    # Load existing per-task active manifest for UPDATE-vs-CREATE keying.
    existing = {}
    archived_prev = []
    if PER_TASK_MANIFEST.exists():
        m = json.loads(PER_TASK_MANIFEST.read_text())
        for e in m.get("active_tests", []):
            existing[(e["task_id"], e["ac_id"])] = e
        archived_prev = m.get("archived_tests", [])

    active_tests = []
    archived_tests = list(archived_prev)
    created, archived_now, skipped = [], [], []
    warnings = []

    for ac in acs:
        ac_id = ac["id"]
        ac_uid = ac["ac_uid"]
        ac_type = ac["type"]
        idn = normalize_id(ac_id)
        rel = f"tests/generated/{TASK_ID}/test_{ac_id}_{ac_uid}.py"
        fpath = ROOT / rel

        # check-schema validation (warn-only; never crash).
        check = ac.get("check", {})
        req = CANONICAL_REQUIRED.get(ac_type, [])
        missing = [k for k in req if k not in check]
        status = "active"
        # These are domain proof-of-call checks; the canonical data shape
        # {file,assertions} is not used here. Keep active but note in stderr.
        if missing:
            warnings.append(
                f"{ac_id} ({ac_uid}): check missing canonical '{ac_type}' keys "
                f"{missing} (domain proof-of-call check; kept active)"
            )

        key = (TASK_ID, ac_id)
        prev = existing.get(key)
        if prev is None:
            fpath.write_text(skeleton(ac))
            created.append(rel)
        elif prev.get("ac_uid") == ac_uid:
            skipped.append(ac_uid)
            # preserve existing entry verbatim, refresh hook_check from source
            prev["hook_check"] = check
            prev["type"] = ac_type
            active_tests.append(prev)
            continue
        else:
            old_uid = prev.get("ac_uid")
            old_rel = prev.get("file")
            old_abs = ROOT / old_rel if old_rel else None
            arch_rel = f"tests/generated/{TASK_ID}/.archive/{old_uid}.py"
            arch_abs = ROOT / arch_rel
            if old_abs and old_abs.exists():
                arch_abs.parent.mkdir(parents=True, exist_ok=True)
                old_abs.rename(arch_abs)
            archived_tests.append({
                "task_id": TASK_ID,
                "ac_id": ac_id,
                "ac_uid": old_uid,
                "archived_at": NOW,
                "reason": "ac_uid changed",
            })
            archived_now.append({"ac_uid": old_uid, "archived_path": arch_rel})
            fpath.write_text(skeleton(ac))
            created.append(rel)

        active_tests.append({
            "task_id": TASK_ID,
            "ac_id": ac_id,
            "ac_uid": ac_uid,
            "type": ac_type,
            "file": rel,
            "status": status,
            "hook_check": check,
        })

    manifest = {
        "schema_version": "1.1",
        "task_id": TASK_ID,
        "generated_at": NOW,
        "active_tests": active_tests,
        "archived_tests": archived_tests,
    }
    PER_TASK_MANIFEST.write_text(json.dumps(manifest, indent=2) + "\n")

    # Upsert global index.
    if GLOBAL_INDEX.exists():
        idx = json.loads(GLOBAL_INDEX.read_text())
    else:
        idx = {"schema_version": "1.0", "kind": "index", "tasks": []}
    idx.setdefault("kind", "index")
    idx.setdefault("tasks", [])
    tasks = idx["tasks"]
    rel_mani = f"tests/generated/{TASK_ID}/manifest.json"
    if not any(t.get("task_id") == TASK_ID for t in tasks):
        tasks.append({"task_id": TASK_ID, "manifest_path": rel_mani})
    else:
        for t in tasks:
            if t.get("task_id") == TASK_ID:
                t["manifest_path"] = rel_mani
    GLOBAL_INDEX.write_text(json.dumps(idx, indent=2) + "\n")

    report = {
        "task_id": TASK_ID,
        "timestamp": NOW,
        "manifest_path": rel_mani,
        "global_index_path": "tests/generated/manifest.json",
        "global_index_kind": "index",
        "trigger_gate": {
            "complexity_tier": data.get("complexity_tier", "COMPLEX"),
            "risk_level": data.get("risk_level", "high"),
            "fired": True,
        },
        "tests_created": created,
        "tests_archived": archived_now,
        "tests_skipped_idempotent": skipped,
        "status": "completed",
    }
    (ROOT / f"docs/dev/test-writer-report-{TASK_ID}.json").write_text(
        json.dumps(report, indent=2) + "\n"
    )

    import sys
    if warnings:
        for w in warnings:
            print("WARN:", w, file=sys.stderr)
    print(json.dumps({
        "created": len(created),
        "archived": len(archived_now),
        "skipped": len(skipped),
        "active_total": len(active_tests),
    }))


if __name__ == "__main__":
    main()
