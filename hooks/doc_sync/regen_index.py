#!/usr/bin/env python3
"""Regenerate INDEX.md for a directory."""

from datetime import datetime, timezone
from pathlib import Path
from .tree import build_tree


def _detect_convention(dir_path: Path) -> str:
    """Detect naming convention from existing files."""
    files = list(dir_path.iterdir())
    if not files:
        return 'kebab'
    lower_count = sum(1 for f in files if f.name.islower())
    kebab_count = sum(1 for f in files if '-' in f.name)
    if kebab_count > len(files) // 2:
        return 'kebab'
    if lower_count == len(files):
        return 'lower'
    return 'kebab'


def _build_stats(dir_path: Path) -> dict:
    total = 0
    dirs = 0
    for item in dir_path.iterdir():
        if item.name in ('INDEX.md', 'README.md', '__init__.py', '.DS_Store'):
            continue
        if item.name.startswith('.'):
            continue
        total += 1
        if item.is_dir():
            dirs += 1
    return {'total': total, 'dirs': dirs}


AUTO_START = '<!-- AUTO:index-stats -->'
AUTO_END = '<!-- /AUTO:index-stats -->'
_FOOTER = '*Auto-generated by doc-sync hook.*'


def _index_needs_update(index_path: Path) -> bool:
    """True only when the INDEX is absent OR already carries the AUTO marker. A markerless
    existing INDEX is left ENTIRELY untouched — this is the regen_readme pattern and is what
    makes annotation-wiping structurally impossible (no markerless file is ever rewritten)."""
    if not index_path.exists():
        return True
    return AUTO_START in index_path.read_text()


def _extract_preserved(old: str, dir_name: str) -> str:
    """Hand-written prose from a MARKED INDEX.md: everything outside the generated regions.
    Only called when the AUTO marker is present (see _index_needs_update), so the generated
    region is unambiguously the AUTO_START..AUTO_END block — no legacy/positional guessing.
    Strips: the leading title line, the AUTO marker block (and any stray marker line), and the
    trailing `---`+footer pair at the very end. Human `---`, `**Convention**:`, or `## Tree`
    prose OUTSIDE the marker block is preserved verbatim. Idempotent."""
    lines = old.splitlines()
    n = len(lines)
    remove = [False] * n
    title = f'# {dir_name}'
    first = 0
    while first < n and not lines[first].strip():
        first += 1
    if first < n and lines[first].rstrip() == title:
        remove[first] = True
    start_idx = end_idx = None
    for idx, ln in enumerate(lines):
        st = ln.strip()
        if st in (AUTO_START, AUTO_END):
            remove[idx] = True
        if st == AUTO_START and start_idx is None:
            start_idx = idx
        elif st == AUTO_END and start_idx is not None and end_idx is None:
            end_idx = idx
    if start_idx is not None and end_idx is not None and end_idx > start_idx:
        for idx in range(start_idx, end_idx + 1):
            remove[idx] = True
    last = n - 1
    while last >= 0 and not lines[last].strip():
        last -= 1
    if last >= 0 and lines[last].strip() == _FOOTER:
        p = last - 1
        while p >= 0 and not lines[p].strip():
            p -= 1
        if p >= 0 and lines[p].strip() == '---':
            remove[last] = True
            remove[p] = True
    preserved = [lines[idx] for idx in range(n) if not remove[idx]]
    while preserved and not preserved[0].strip():
        preserved.pop(0)
    while preserved and not preserved[-1].strip():
        preserved.pop()
    return '\n'.join(preserved)


def _build_index_content(dir_path: Path, convention: str, preserved: str = '') -> str:
    tree = build_tree(dir_path, max_depth=3)
    ts = datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')
    lines = [
        f'# {dir_path.name}',
        '',
        AUTO_START,
        f'*Last updated: {ts}*',
        f'**Total entries**: {len(tree)}',
        f'**Convention**: {convention}',
        '',
        '## Tree',
        '```',
        f'{dir_path.name}/',
    ]
    lines.extend(tree)
    lines.extend(['```', AUTO_END])
    if preserved.strip():
        lines.extend(['', preserved])
    lines.extend(['', f'---\n{_FOOTER}'])
    return '\n'.join(lines)


def regen_index(dir_path: Path):
    """Regenerate INDEX.md, preserving hand-written annotations. Only files that already
    carry the AUTO marker (or do not exist yet) are managed; a markerless existing INDEX is
    left untouched, mirroring regen_readme so annotation-wiping is structurally impossible."""
    index_path = dir_path / 'INDEX.md'
    if not _index_needs_update(index_path):
        return
    convention = _detect_convention(dir_path)
    preserved = ''
    if index_path.exists():
        preserved = _extract_preserved(index_path.read_text(), dir_path.name)
    content = _build_index_content(dir_path, convention, preserved)
    index_path.write_text(content)
