#!/usr/bin/env python3
"""Regenerate INDEX.md for a directory."""

from datetime import datetime, timezone
from pathlib import Path
from .tree import build_tree


def _detect_convention(dir_path: Path) -> str:
    """Detect naming convention from existing files."""
    files = list(dir_path.iterdir())
    if not files:
        return 'kebab'
    lower_count = sum(1 for f in files if f.name.islower())
    kebab_count = sum(1 for f in files if '-' in f.name)
    if kebab_count > len(files) // 2:
        return 'kebab'
    if lower_count == len(files):
        return 'lower'
    return 'kebab'


def _build_stats(dir_path: Path) -> dict:
    total = 0
    dirs = 0
    for item in dir_path.iterdir():
        if item.name in ('INDEX.md', 'README.md', '__init__.py', '.DS_Store'):
            continue
        if item.name.startswith('.'):
            continue
        total += 1
        if item.is_dir():
            dirs += 1
    return {'total': total, 'dirs': dirs}


AUTO_START = '<!-- AUTO:index-stats -->'
AUTO_END = '<!-- /AUTO:index-stats -->'
_FOOTER = '*Auto-generated by doc-sync hook.*'


def _extract_preserved(old: str, dir_name: str) -> str:
    """Return hand-written prose from an existing INDEX.md — everything that is NOT
    part of the auto-generated skeleton (title, stat lines, the ## Tree fenced block,
    the AUTO markers, and the footer). This is what lets human annotations (e.g. a
    naming-convention `>` note or an extra `## Notes` section) survive a regen instead
    of being clobbered. Idempotent: the patterns stripped here are exactly the ones
    _build_index_content emits, so re-running regen on our own output preserves the
    same prose and never duplicates the skeleton."""
    lines = old.splitlines()
    preserved: list[str] = []
    i, n = 0, len(lines)
    title = f'# {dir_name}'
    while i < n:
        s = lines[i].strip()
        if i == 0 and lines[i].rstrip() == title:
            i += 1
            continue
        if s in (AUTO_START, AUTO_END):
            i += 1
            continue
        if s.startswith('*Last updated:') or s.startswith('**Total entries**:') \
                or s.startswith('**Convention**:'):
            i += 1
            continue
        if s == '## Tree':
            i += 1
            if i < n and lines[i].strip().startswith('```'):
                i += 1
                while i < n and not lines[i].strip().startswith('```'):
                    i += 1
                if i < n:  # consume closing fence
                    i += 1
            continue
        if s == '---':
            i += 1
            if i < n and lines[i].strip() == _FOOTER:
                i += 1
            continue
        if s == _FOOTER:
            i += 1
            continue
        preserved.append(lines[i])
        i += 1
    while preserved and not preserved[0].strip():
        preserved.pop(0)
    while preserved and not preserved[-1].strip():
        preserved.pop()
    return '\n'.join(preserved)


def _build_index_content(dir_path: Path, convention: str, preserved: str = '') -> str:
    tree = build_tree(dir_path, max_depth=3)
    ts = datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')
    lines = [
        f'# {dir_path.name}',
        '',
        AUTO_START,
        f'*Last updated: {ts}*',
        f'**Total entries**: {len(tree)}',
        f'**Convention**: {convention}',
        '',
        '## Tree',
        '```',
        f'{dir_path.name}/',
    ]
    lines.extend(tree)
    lines.extend(['```', AUTO_END])
    if preserved.strip():
        lines.extend(['', preserved])
    lines.extend(['', f'---\n{_FOOTER}'])
    return '\n'.join(lines)


def regen_index(dir_path: Path):
    """Regenerate INDEX.md for a directory, preserving hand-written annotations.

    The auto region (stats + tree) is wrapped in AUTO markers and refreshed on every
    run; any hand-written prose outside that region is extracted from the existing file
    and re-attached, so doc-sync keeps the tree current without wiping human notes."""
    index_path = dir_path / 'INDEX.md'
    convention = _detect_convention(dir_path)
    preserved = ''
    if index_path.exists():
        preserved = _extract_preserved(index_path.read_text(), dir_path.name)
    content = _build_index_content(dir_path, convention, preserved)
    index_path.write_text(content)
